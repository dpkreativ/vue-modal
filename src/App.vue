<template>
  <button
    class="bg-white py-4 px-12 rounded-xl"
    type="button"
    @click="showModal"
  >
    Open Modal
  </button>
  <Modal v-show="isModalVisible" />
</template>

<script>
import Modal from "./components/Modal.vue";

export default {
  name: "App",
  components: {
    Modal,
  },
  data() {
    return {
      isModalVisible: false,
    };
  },
  methods: {
    showModal() {
      this.isModalVisible = true;
    },
    closeModal() {
      this.isModalVisible = false;
    },
  },
};
</script>

